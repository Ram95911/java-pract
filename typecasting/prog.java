class prog {
    public static void main(String[] args) {
        // automatic type casting
        int ab = 12;
        double c = ab; // int to double
        String d = "112";
        System.out.println(d);

        System.out.println("=====================");
        System.out.println("Manual Type Casting.");

        double mydouble=12.23;
        int myint=(int) mydouble;

        System.out.println(myint+"------------"+mydouble);


        
    }
}
